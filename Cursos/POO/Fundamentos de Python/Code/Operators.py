# (+) Suma
# (-) Resta
# (*) Multiplicación
# (/) División
# (%) Modulo o Residuo
# (//) Division con valor entero
# (**) Exponenciación

# Suma
print(2 + 2)

# Resta
print(2 - 2)

# Multiplicación
print(2 * 2)

# División
print(2 / 2)

# Modulo o Residuo
print(2 % 2)

# Division con valor entero
print(2 // 2)

# Exponenciación
print(2 ** 2)



# Operadores de comparacion
# < Menor que
# > Mayor que
# <= Menor o igual que
# >= Mayor o igual que
# == Igual que
# != Diferente de

# Menor que
print(2 < 2)

# Mayor que
print(2 > 2)

# Menor o igual que
print(2 <= 2)

# Mayor o igual que
print(2 >= 2)

# Igual que
print(2 == 2)

# Diferente de
print(2 != 2)

